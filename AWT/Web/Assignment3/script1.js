// Implicit Type Conversion
let implicitConversion = '5' + 5; 
document.write("Implicit Conversion ('5' + 5): " + implicitConversion + "<br>");

let implicitConversion2 = '5' * 5; 
document.write("Implicit Conversion ('5' * 5): " + implicitConversion2 + "<br>");

// Explicit Type Conversion
let explicitConversion = Number('5') + 5; 
document.write("Explicit Conversion (Number('5') + 5): " + explicitConversion + "<br>");

let explicitConversion2 = String(5) + 5; 
document.write("Explicit Conversion (String(5) + 5): " + explicitConversion2 + "<br>");
