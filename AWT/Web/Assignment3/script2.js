let str = "Hello, JavaScript!";

document.write("Length of the string: " + str.length + "<br>"); // String length
document.write("Uppercase: " + str.toUpperCase() + "<br>"); // Convert to uppercase
document.write("Lowercase: " + str.toLowerCase() + "<br>"); // Convert to lowercase
document.write("Substring (0, 5): " + str.substring(0, 5) + "<br>"); // Get substring
document.write("Replace 'JavaScript' with 'World': " + str.replace("JavaScript", "World") + "<br>");
